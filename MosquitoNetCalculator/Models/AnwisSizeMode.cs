namespace MosquitoNetCalculator.Models
{
    /// <summary>
    /// Режим выбора типа размера для товара Anwis.
    /// Определяет две независимые коррекции размеров:
    ///   — расчётную (влияет на площадь и стоимость через GetCalcWidth/GetCalcHeight),
    ///   — копировальную (текст «На завод» через GetCopyWidth/GetCopyHeight).
    /// </summary>
    public enum AnwisSizeMode
    {
        /// <summary>Брусбокс 60 мм — расчёт: W+2, H−30; копирование: W−20, H−20.</summary>
        Брусбокс60 = 0,

        /// <summary>Брусбокс 70 мм — расчёт: W−2, H−30; копирование: W−20, H−20.</summary>
        Брусбокс70 = 1,

        /// <summary>Профипласт — расчёт: без изм.; копирование: W−20, H−20.</summary>
        Профипласт = 2,

        /// <summary>Размер проёма — расчёт: W+20, H+20; копирование: без изм.</summary>
        РазмерПроёма = 3,

        /// <summary>Габаритный — расчёт: без изм.; копирование: W−20, H−20.</summary>
        Габаритный = 4
    }
}
